            <th>Swapping Audio Samples</th>
            <td>To replace a sequencer module's audio sample, click the <strong>button featuring the file name</strong> of the sample you'd like to replace. You can also <strong>drag and drop a new sample onto a sequencer module</strong> to replace its sample.</td>
        </tr>
        <tr>
            <th>Adding and Removing Sequence Steps</th>
            <td>Click the <strong>+ and - buttons</strong> in a sequencer module to add or remove steps to the module's sequence. Each module can contain as many or as few steps as you like.</td>
        </tr>
        <tr>
            <th>Removing Sequencer Modules</th>
            <td>Remove a sequencer module by clicking the <strong>X in the top right of the module</strong> you wish to delete.</td>
        </tr>
        <tr>
            <th>Changing Loop Direction</th>
            <td>Clicking the <strong>loop button in the app bar</strong> will toggle between three loop modes: <strong>Loop Forward, Loop Backward, and Loop Ping Pong</strong>. This is a global setting for all sequencer modules.</td>
        </tr>
        <tr>
            <th>Play/Stop Button</th>
            <td>On play, all sequences will start from the beginning. On stop all sequencers cease playing.</td>
        </tr>
        <tr>
            <th>Adjusting BPM</th>
            <td>On smaller devices, clicking the <strong>beats per minnute button in the app bar</strong> will open a dialog featuring a knob to adjust this parameter. On larger device, the knob will appear in the app bar. <strong>Click and drag up or down on the knob</strong> to adjust the <abbr title="beats per minute">bpm</abbr>, or <strong>click the number</strong> to edit it directly.</td>
        </tr>
        <tr>
            <th>Adding Additional Sequencer Modules</th>
            <td>To add another sequencer module, click the <strong>add sequence button in the app bar</strong>. You'll then be prompted to select an audio file from your device. Another sequencer module will then appear at the end of the exsisting group of sequencers.</td>
        </tr>    
